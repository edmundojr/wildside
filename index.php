			<h2>Powerscout Waterfall</h2>
			<p>We start the journey in Enniskerry, a small village 5 minutes south of the Dublin border, in the foothills of the Wicklow Mountains. We were planning to walk from there to Powerscourt Gardens and then take a trail to Powerscourt Waterfall. Sadly our map was inaccurate, and the trails and roads on the Gardens were private. We had to take a massive detour, but since we're in Ireland, the route was very charming.</p>
			<p>We were famished by the time we reached the Waterfall, so I ordered cheese and chips to eat with the sandwiches. They have a little kiosk that sells almost anything you'd expected to find in a kiosk – maybe a bit more.</p>
			<p>Powerscourt Waterfall is splendidly beautiful, at 121m is Ireland's highest waterfall. The weather was nice and warm, so there was loads of people enjoying some sun time. We relaxed and took some pictures, while getting mentally ready for the next round.</p>
			<h2>Djouce Woods and Djouce Mountain</h2>
			<p>Upon arriving in the waterfall, we asked the gatekeeper if it was possible to walk through Djouce Woods’ tracks. He said that the way was closed, but there was a section of the fence that had fell over, and maybe we could try there: “Just go over it”, he said.</p>
			<p>It was there that our adventure truly begun, for the tracks were all old and unused, and there was so many of them that I had to consult the map all the time. We're tired and eager to get to the campsite before nighttime, so we pressed ourselves to keep going. Finally we found our way to the other side and to the Wicklow Way, what a relieve!</p>
			<p>We set up our tent amongst millions of midges and other bugs – seriously, I'd never seen so many insects together – on the base of Djouce Mountain (380m),  right on the middle of that spectacular scenery! You could see the Irish sea between Bray and Greystones, just astonishing.</p>
			<p>The rain had not stopped all night, and then <strong>we heard the screams</strong>: angry or maybe terrified, one guy was searching/hunting someone, we still don't know each one it was for sure. That startled us for a bit, so I went outside to check the whereabouts. I saw a couple of guys on top of the Maulin Mountain, (probably) searching for a missing friend which apparently they'd found, because all of a sudden the screams stopped.</p>
			<h2>The Invisible Lough Tay</h2>
			<p>Early next morning we broke camp and moved on to the Wicklow Way. It was a cool and windy morning when we start moving. Unfortunately started to rain and in 5 minutes time we were soaking wet. The rain and fog persisted through the mountains, so Laura wasn't able to see <strong>Lough Tay</strong>, the beautiful lake that is part owned by the Guinness Family, and where History Channel recorded their hit series Vikings.</p>
