			<p>Last weekend Laura and I went camping in the Wicklow Mountains. It was Laura's first time sleeping on a tent, and I could see that although excited, she seemed scared too.</p>
			<p>As for me, I've always loved the great outdoors, even though I hadn't had a chance to see the <em>really great outdoors</em> with mountains that go all the way through the clouds.</p>
			<p>Laura on the other hand is more of a city-girl. She likes comfort as much as the next city-girl: sleep in a soft bed, take a warm bath and enjoy her meals <em>preferably</em> hot. By all means, there's nothing wrong with that, we're just different in this matter.</p>
			<p>So when she told me she wanted go along with me in one of my hikes, I felt surprised, but very content, for is nice to have a partner with you on long hikes – specially someone that <em>is</em> your partner. We decided to go on a two day hike to Glendalough, Co. Wicklow.</p>
			<p>Backpacks packed, shoes ready, sandwiches sandwiched, canteen filled: it was time to go.</p>
